    private boolean dnivalido(){
        String dni1 = dni.getText();
        Pattern dnis = Pattern.compile("^[0-9]{8}[QWRTYPSDFGHJKLÃ‘ZXCVBNM]$");
        Matcher mdni= dnis.matcher(dni1);
        if(mdni.matches()){
            return true;
        }
        else{
            return false;
        }
    
        
    
    }
    
