        private boolean nombrevalido(){
        String nombre1 = nombre.getText();
        Pattern nombres = Pattern.compile("^([A-Za-z]+)");
        Matcher mnombre= nombres.matcher(nombre1);
        if(mnombre.matches()){
            return true;
        }
        else{
            return false;
        }
    
    
    }
    
    
    
